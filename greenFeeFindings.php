<h2>Student Green Fees</h2>
<p>	Student green fees are a funding option for environmentally friendly initiatives that have seen enormous success almost everywhere they have been implemented.</p> <p>Some green fees work by matching with state funds, grant funds, or university budgets, as a means of ameliorating cost for large-scale projects and simultaneously demonstrating student mandate for, and commitment to, the projects. Augsburg College implemented 100% wind energy purchasing using a student green fee for 50% of the necessary funds. Some Colleges that find resistance to the idea of mandating environmental responsibility on their politically diverse campuses provide an “Opt-out” green fee system. Lewis and Clark instated a $20 green fee, but to allow for student choice, did not make it mandatory. Since most students did not consider it worth the trouble to opt-out of it, the fee achieved 95% participation, and thereby provides the largest green fee in the sample below, amounting to $85 per year, allowing the school, among other things, to purchase 30% of their energy from clean sources.</p>
